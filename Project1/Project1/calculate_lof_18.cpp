#include <iostream>
using namespace std;

int main_18()
{
	int a = 100;
	// =
	a = 10;
	cout << "a=" << a << endl;

	// +=
	a = 10;
	a += 10;
	cout << "a=" << a << endl;

	// -=
	a = 10;
	a -= 10;
	cout << "a=" << a << endl;
	
	// *=
	a = 10;
	a *= 5;
	cout << "a=" << a << endl;

	// /=
	a = 10;
	a /= 3;
	cout << "a=" << a << endl;

	// %=
	a = 10;
	a %= 3;
	cout << "a=" << a << endl;

	system("pause");
	return 0;
}